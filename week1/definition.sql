-- 1.1.1 Database
-- Definition: A database is a structured collection of data that is organized in a way that it can be easily accessed, managed, and updated. It typically consists of tables, queries, indexes, and other objects.

-- 1.1.2 Table
-- Definition: A table is a collection of related data organized in rows and columns. Each row represents a record, and each column represents a field or attribute.

-- 1.1.3 Record
-- Definition: A record, also known as a row, is a single instance of data stored in a database table. It contains values corresponding to each field defined in the table.

-- 1.1.4 Field
-- Definition: A field, also known as a column, is a single piece of data within a database table. It represents a specific attribute or characteristic of the data being stored.

-- 1.1.5 Primary Key
-- Definition: A primary key is a unique identifier for each record in a database table. It ensures that each row in the table can be uniquely identified and retrieved.

-- 1.1.6 SQL
-- Definition: SQL (Structured Query Language) is a standard programming language used for managing and manipulating relational databases. It is used to perform tasks such as querying data, updating records, and creating database objects.

-- 1.1.7 Query
-- Definition: A query is a request for data or information from a database. It is written in SQL and executed against the database to retrieve specific records or perform operations on the data.

-- 1.1.8 Index
-- Definition: An index is a data structure that improves the speed of data retrieval operations on a database table. It contains keys built from one or more columns of the table, allowing for faster lookup and retrieval of data.

-- 1.1.9 Normalization
-- Definition: Normalization is the process of organizing data in a database to reduce redundancy and improve data integrity. It involves dividing large tables into smaller, related tables and defining relationships between them.

-- 1.1.10 Database Management System (DBMS)
-- Definition: A Database Management System (DBMS) is software that is used to manage, manipulate, and organize data in a database. It provides tools and utilities for creating, querying, updating, and administering databases.
